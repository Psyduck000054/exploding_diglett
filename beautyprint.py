import pygame

def print_1d (arr):
    print ("[", end = " ")
    for d0 in arr:
        print (d0, end = " ")
    print ("]")


def print_2d (arr):
    print ("-------------------------------------------------------------------------------------------------------------------------------")
    for d1 in arr:
        print_1d(d1)
        print ("-------------------------------------------------------------------------------------------------------------------------------")


def print_3d (arr):
    print ("-------------------------------------------------------------------------------------------------------------------------------")
    print ("-------------------------------------------------------------------------------------------------------------------------------")
    for d2 in arr:
        print_2d(d2)
        print ("-------------------------------------------------------------------------------------------------------------------------------")
        print ("-------------------------------------------------------------------------------------------------------------------------------")
